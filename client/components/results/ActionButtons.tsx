import { RotateCcw, Download } from "lucide-react";

interface ActionButtonsProps {
  onRegenerate: () => void;
  onDownload?: () => void;
}

export function ActionButtons({
  onRegenerate,
  onDownload,
}: ActionButtonsProps) {
  const handleDownload = () => {
    if (onDownload) {
      onDownload();
    } else {
      // Default download handler - creates a JSON file with results
      const element = document.createElement("a");
      const file = new Blob(
        [
          JSON.stringify(
            {
              timestamp: new Date().toISOString(),
              note: "Career guidance results - Generated by AI Analysis",
            },
            null,
            2
          ),
        ],
        { type: "application/json" }
      );
      element.href = URL.createObjectURL(file);
      element.download = `career-insights-${new Date().getTime()}.json`;
      document.body.appendChild(element);
      element.click();
      document.body.removeChild(element);
    }
  };

  return (
    <div className="mt-12 p-8 bg-gradient-to-r from-gray-50 to-gray-100 rounded-xl border border-gray-200">
      <h3 className="text-xl font-bold text-gray-900 mb-6">Next Steps</h3>

      <div className="flex flex-col sm:flex-row gap-4">
        <button
          onClick={onRegenerate}
          className="flex-1 flex items-center justify-center gap-2 px-6 py-3 bg-gradient-to-r from-indigo-600 to-purple-600 text-white font-semibold rounded-lg hover:shadow-lg hover:from-indigo-700 hover:to-purple-700 transition-all duration-300"
        >
          <RotateCcw size={20} />
          Regenerate Results
        </button>

        <button
          onClick={handleDownload}
          className="flex-1 flex items-center justify-center gap-2 px-6 py-3 bg-white text-gray-900 font-semibold rounded-lg border border-gray-300 hover:bg-gray-50 hover:shadow-lg transition-all duration-300"
        >
          <Download size={20} />
          Download Results
        </button>
      </div>

      <p className="mt-6 text-gray-600 text-center">
        ðŸ’¡ <strong>Tip:</strong> Save these results and revisit them as you
        progress through your career. Your interests and goals may evolve, and
        that's completely normal!
      </p>
    </div>
  );
}
